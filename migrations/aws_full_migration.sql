--
-- Name: story_participation_by_school_period; Type: VIEW; Schema: public; Owner: -
--

CREATE VIEW public.story_participation_by_school_period AS
 WITH story_sessions AS (
         SELECT sa.school_id,
            to_char((sa.date)::timestamp with time zone, 'YYYY-MM'::text) AS period_month,
            count(*) AS sessions_count
           FROM public.story_activities sa
          GROUP BY sa.school_id, (to_char((sa.date)::timestamp with time zone, 'YYYY-MM'::text))
        ), published_stories AS (
         SELECT sl.school_id,
            to_char(COALESCE(sl.published_at, sl.created_at), 'YYYY-MM'::text) AS period_month,
            count(*) AS stories_published
           FROM public.story_library sl
          WHERE (sl.publish_status = 'published'::text)
          GROUP BY sl.school_id, (to_char(COALESCE(sl.published_at, sl.created_at), 'YYYY-MM'::text))
        ), published_anthologies AS (
         SELECT sa.school_id,
            to_char(COALESCE(sa.published_at, sa.created_at), 'YYYY-MM'::text) AS period_month,
            count(*) AS anthologies_published
           FROM public.story_anthologies sa
          WHERE ((sa.publish_status = 'published'::text) AND (sa.school_id IS NOT NULL))
          GROUP BY sa.school_id, (to_char(COALESCE(sa.published_at, sa.created_at), 'YYYY-MM'::text))
        ), periods AS (
         SELECT story_sessions.school_id,
            story_sessions.period_month
           FROM story_sessions
        UNION
         SELECT published_stories.school_id,
            published_stories.period_month
           FROM published_stories
        UNION
         SELECT published_anthologies.school_id,
            published_anthologies.period_month
           FROM published_anthologies
        )
 SELECT p.school_id,
    p.period_month,
    COALESCE(ss.sessions_count, (0)::bigint) AS sessions_count,
    COALESCE(ps.stories_published, (0)::bigint) AS stories_published,
    COALESCE(pa.anthologies_published, (0)::bigint) AS anthologies_published,
        CASE
            WHEN ((COALESCE(ss.sessions_count, (0)::bigint) > 0) OR (COALESCE(ps.stories_published, (0)::bigint) > 0) OR (COALESCE(pa.anthologies_published, (0)::bigint) > 0)) THEN 1
            ELSE 0
        END AS active_flag
   FROM (((periods p
     LEFT JOIN story_sessions ss ON (((ss.school_id = p.school_id) AND (ss.period_month = p.period_month))))
     LEFT JOIN published_stories ps ON (((ps.school_id = p.school_id) AND (ps.period_month = p.period_month))))
     LEFT JOIN published_anthologies pa ON (((pa.school_id = p.school_id) AND (pa.period_month = p.period_month))));


