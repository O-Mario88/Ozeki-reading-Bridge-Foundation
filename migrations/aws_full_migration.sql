--
-- Name: teaching_quality_by_school_period; Type: VIEW; Schema: public; Owner: -
--

CREATE VIEW public.teaching_quality_by_school_period AS
 SELECT school_id,
    to_char((lesson_date)::timestamp with time zone, 'YYYY-MM'::text) AS period_month,
    count(*) AS evaluations_count,
    avg(overall_score) AS average_overall_score,
    sum(
        CASE
            WHEN (overall_level = 'Strong'::text) THEN 1
            ELSE 0
        END) AS strong_count,
    sum(
        CASE
            WHEN (overall_level = 'Good'::text) THEN 1
            ELSE 0
        END) AS good_count,
    sum(
        CASE
            WHEN (overall_level = 'Developing'::text) THEN 1
            ELSE 0
        END) AS developing_count,
    sum(
        CASE
            WHEN (overall_level = 'Needs Support'::text) THEN 1
            ELSE 0
        END) AS needs_support_count
   FROM public.lesson_evaluations le
  WHERE (status = 'active'::text)
  GROUP BY school_id, (to_char((lesson_date)::timestamp with time zone, 'YYYY-MM'::text));


--
-- Name: teaching_learning_alignment_by_school_period; Type: VIEW; Schema: public; Owner: -
--

CREATE VIEW public.teaching_learning_alignment_by_school_period AS
 WITH assessment_by_period AS (
         SELECT ar.school_id,
            to_char((ar.assessment_date)::timestamp with time zone, 'YYYY-MM'::text) AS period_month,
            count(*) AS sample_size,
            avg(ar.decodable_words_score) AS decoding_avg,
            avg(ar.story_reading_score) AS fluency_avg,
            avg(ar.reading_comprehension_score) AS comprehension_avg,
                CASE
                    WHEN (count(ar.story_reading_score) > 0) THEN (((sum(
                    CASE
                        WHEN (ar.story_reading_score < (20)::double precision) THEN 1
                        ELSE 0
                    END))::numeric * 100.0) / (count(ar.story_reading_score))::numeric)
                    ELSE NULL::numeric
                END AS non_reader_pct,
                CASE
                    WHEN (count(ar.story_reading_score) > 0) THEN (((sum(
                    CASE
                        WHEN (ar.story_reading_score >= (20)::double precision) THEN 1
                        ELSE 0
                    END))::numeric * 100.0) / (count(ar.story_reading_score))::numeric)
                    ELSE NULL::numeric
                END AS cwpm20_plus_pct
           FROM public.assessment_records ar
          GROUP BY ar.school_id, (to_char((ar.assessment_date)::timestamp with time zone, 'YYYY-MM'::text))
        ), periods AS (
         SELECT teaching_quality_by_school_period.school_id,
            teaching_quality_by_school_period.period_month
           FROM public.teaching_quality_by_school_period
        UNION
         SELECT assessment_by_period.school_id,
            assessment_by_period.period_month
           FROM assessment_by_period
        UNION
         SELECT story_participation_by_school_period.school_id,
            story_participation_by_school_period.period_month
           FROM public.story_participation_by_school_period
        )
 SELECT p.school_id,
    p.period_month,
    tq.evaluations_count AS teaching_evaluations_count,
    tq.average_overall_score AS teaching_quality_avg,
    abp.sample_size AS learner_sample_size,
    abp.decoding_avg,
    abp.fluency_avg,
    abp.comprehension_avg,
    abp.non_reader_pct,
    abp.cwpm20_plus_pct,
    COALESCE(sp.sessions_count, (0)::bigint) AS story_sessions_count,
    COALESCE(sp.stories_published, (0)::bigint) AS stories_published_count,
    COALESCE(sp.anthologies_published, (0)::bigint) AS anthologies_published_count,
    COALESCE(sp.active_flag, 0) AS story_active_flag
   FROM (((periods p
     LEFT JOIN public.teaching_quality_by_school_period tq ON (((tq.school_id = p.school_id) AND (tq.period_month = p.period_month))))
     LEFT JOIN assessment_by_period abp ON (((abp.school_id = p.school_id) AND (abp.period_month = p.period_month))))
     LEFT JOIN public.story_participation_by_school_period sp ON (((sp.school_id = p.school_id) AND (sp.period_month = p.period_month))));


