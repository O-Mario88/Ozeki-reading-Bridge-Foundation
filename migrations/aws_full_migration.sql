--
-- Name: school_insights_rollup; Type: VIEW; Schema: public; Owner: -
--

CREATE VIEW public.school_insights_rollup AS
 WITH scoped AS (
         SELECT ai.insights_id,
            ai.scope_id AS school_id,
            ai.key_findings,
            ai.conclusions_next_steps,
            ai.updated_at
           FROM public.activity_insights ai
          WHERE (ai.scope_type = 'school'::text)
        ), latest AS (
         SELECT scoped.school_id,
            max(scoped.updated_at) AS last_updated
           FROM scoped
          GROUP BY scoped.school_id
        ), latest_findings AS (
         SELECT s.school_id,
            string_agg(TRIM(BOTH FROM COALESCE(s.key_findings, ''::text)), ', '::text ORDER BY s.insights_id) AS latest_findings
           FROM (scoped s
             JOIN latest l_1 ON (((l_1.school_id = s.school_id) AND (l_1.last_updated = s.updated_at))))
          GROUP BY s.school_id
        ), actions AS (
         SELECT grouped.school_id,
            string_agg(grouped.rec_id, ', '::text ORDER BY grouped.rec_id) AS recommendation_ids,
            string_agg(grouped.action_text, ', '::text ORDER BY grouped.action_text) AS open_actions
           FROM ( SELECT DISTINCT ai.scope_id AS school_id,
                    ar.rec_id,
                    (((ar.rec_id || ':'::text) || ar.priority) ||
                        CASE
                            WHEN (COALESCE(TRIM(BOTH FROM ar.notes), ''::text) <> ''::text) THEN ((' ('::text || TRIM(BOTH FROM ar.notes)) || ')'::text)
                            ELSE ''::text
                        END) AS action_text
                   FROM (public.activity_insights ai
                     LEFT JOIN public.activity_recommendations ar ON ((ar.insights_id = ai.insights_id)))
                  WHERE ((ai.scope_type = 'school'::text) AND (ar.rec_id IS NOT NULL))) grouped
          GROUP BY grouped.school_id
        )
 SELECT (l.school_id)::integer AS school_id,
    l.last_updated,
    COALESCE(lf.latest_findings, ''::text) AS latest_findings,
    COALESCE(a.open_actions, ''::text) AS open_actions,
    COALESCE(a.recommendation_ids, ''::text) AS recommendation_ids
   FROM ((latest l
     LEFT JOIN latest_findings lf ON ((lf.school_id = l.school_id)))
     LEFT JOIN actions a ON ((a.school_id = l.school_id)));


