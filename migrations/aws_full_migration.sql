-- Name: impact_public_school_scope; Type: VIEW; Schema: public; Owner: -
--

CREATE VIEW public.impact_public_school_scope AS
 SELECT id AS school_id,
    school_uid,
    school_code,
    name AS school_name,
    district,
    COALESCE(sub_county, ''::text) AS sub_county,
    COALESCE(sub_region, ''::text) AS sub_region,
    COALESCE(region, ''::text) AS region,
    COALESCE(district_id, ''::text) AS district_id,
    COALESCE(subregion_id, ''::text) AS subregion_id,
    COALESCE(region_id, ''::text) AS region_id,
        CASE
            WHEN (COALESCE(enrollment_total, 0) > 0) THEN COALESCE(enrollment_total, 0)
            WHEN ((COALESCE(enrolled_boys, 0) + COALESCE(enrolled_girls, 0)) > 0) THEN (COALESCE(enrolled_boys, 0) + COALESCE(enrolled_girls, 0))
            ELSE COALESCE(enrolled_learners, 0)
        END AS enrollment_total,
    (((((COALESCE(enrolled_baby, 0) + COALESCE(enrolled_middle, 0)) + COALESCE(enrolled_top, 0)) + COALESCE(enrolled_p1, 0)) + COALESCE(enrolled_p2, 0)) + COALESCE(enrolled_p3, 0)) AS direct_impact_total
   FROM public.schools_directory;


