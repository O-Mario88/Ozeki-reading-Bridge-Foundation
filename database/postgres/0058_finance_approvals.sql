-- ============================================================================
-- 0058 — Finance approvals, segregation of duties, period locks, audit chain
-- ============================================================================
-- Institutional-grade controls:
--   * approval_thresholds: configurable amount bands -> required approver roles
--   * finance_approvals:   signed approval events per expense (one row per role)
--   * finance_period_locks: once closed, no retroactive edits
--   * finance_audit_chain: hash-chained log of every finance state change
-- All tables additive; existing finance_expenses/receipts untouched.
-- ============================================================================

-- Thresholds: above each amount (UGX), the listed role chain must approve in order.
CREATE TABLE IF NOT EXISTS finance_approval_thresholds (
  id              INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  min_amount_ugx  NUMERIC(14, 2) NOT NULL,
  required_roles  TEXT[] NOT NULL,
  description     TEXT,
  is_active       BOOLEAN NOT NULL DEFAULT TRUE,
  created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW()
);
CREATE INDEX IF NOT EXISTS idx_approval_thresholds_amount
  ON finance_approval_thresholds (min_amount_ugx DESC)
  WHERE is_active IS TRUE;

